class Testdijkstra_spt(TestCase):

    def test_dijkstra_spt_1(self):
        self.assertEqual(
            ({'A': 0, 'B': 1},
             {'A': {('B', 1)},
              'B': {('A', 1)}}),
            dijkstra_spt('A', {
                'A': {('B', 1)},
                'B': {('A', 1)}
            }))

